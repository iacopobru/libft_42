#include "libft.h"

char	*ft_strnstr(const char *big, const char *little, size_t len)
{
	size_t i;
	size_t j;

	i = 0;
	if (*little == '\0')
		return ((char *)big);
	while (i < len && big[i] != '\0')
	{
		j = 0;
		while (big[i] == little[j] && len > i++ && little[j] != '\0')
			j++;
		if (little[j] == '\0')
			return ((char *)big + i - j);
	}
	return (NULL);
}